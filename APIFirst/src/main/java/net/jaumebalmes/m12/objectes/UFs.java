package net.jaumebalmes.m12.objectes;

public class UFs {
	private String nom;
	private int posicio;
	
	public UFs(String nom, int posicio) {
		super();
		this.nom = nom;
		this.posicio = posicio;
	}

	public String getNom() {
		return nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}

	public int getPosicio() {
		return posicio;
	}

	public void setPosicio(int posicio) {
		this.posicio = posicio;
	}
	
}

